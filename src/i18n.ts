export type StrKey = keyof typeof STR.ru;

export function t(lang: Language, key: StrKey): string {
  return STR[lang][key] ?? STR.ru[key];
}

/** Reactive translator bound to the current strip language */
export function useT(): (key: StrKey) => string {
  const lang = useComicStore(s => s.strip.language);
  return key => t(lang, key);
}
